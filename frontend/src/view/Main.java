    private void btn_cadastrar_cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cadastrar_cadastrarActionPerformed
        // Método cadastrar que retorna um boolean 
        boolean cadastrado = PersonalController.cadastro(input_nome_cadastrar.getText(), input_email_cadastrar.getText(), input_senha_cadastrar.getText(), input_confirmar_senha_cadastrar.getText(), input_data_nasc_cadastrar.getText(), input_telefone_cadastrar.getText(), input_cr_cadastrar.getText(), input_endereco_cadastrar.getText());
        //Se cadastrado com sucesso aparece a mendagem e retorna pra tela de login
        if(cadastrado){
            JOptionPane.showMessageDialog(this,"CADASTRO BEM SUCEDIDO", "Mensagem do sistema", JOptionPane.PLAIN_MESSAGE);
            ((CardLayout) main.getLayout()).show(main, panel_login.getName()); // Mudando o panel do "main"
        }else{
            JOptionPane.showMessageDialog(this,"ERRO AO CADASTRAR", "Mensagem do sistema", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_cadastrar_cadastrarActionPerformed

