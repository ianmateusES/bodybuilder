    private void btn_entrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_entrarActionPerformed
        //Método logar que retorna um boolean
        boolean logado = SessionController.login(select_profissao.getSelectedItem(), input_email_login.getText(), input_senha_login.getText());
        //Se logado com sucesso abre a tela inicial do personal e fecha essa
        if(logado){
            AccessMainPersonal.getInstance().setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this,"ERRO AO ENTRAR", "Mensagem do sistema", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_entrarActionPerformed

