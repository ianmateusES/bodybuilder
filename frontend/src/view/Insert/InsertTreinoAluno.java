    private void btn_inserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_inserirActionPerformed
        
        ArrayList<ExercicioList> exercicios = new ArrayList<ExercicioList>();
        for(int i = 0; i < tabela.getRowCount(); i++){
            System.out.println(tabela.getRowCount());
            
            ExercicioList e = new ExercicioList();
            e.setDivision(tabela.getValueAt(i, 2).toString());
            e.setExercise(tabela.getValueAt(i, 0).toString());
            e.setMethodology(tabela.getValueAt(i, 3).toString());
            e.setSeries(Integer.parseInt(tabela.getValueAt(i, 4).toString()));
            e.setRepetitions(Integer.parseInt(tabela.getValueAt(i, 5).toString()));
            e.setComments(tabela.getValueAt(i, 6).toString());
            exercicios.add(e);
        }

        
        if(TreinoAlunoController.cadastrarTreinoAluno(id_aluno, input_email.getText(), exercicios)){
            JOptionPane.showMessageDialog(this,"CADASTRO BEM SUCEDIDO", "Mensagem do sistema", JOptionPane.PLAIN_MESSAGE);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this,"ERRO AO CADASTRAR", "Mensagem do sistema", JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_btn_inserirActionPerformed

