     private void ListarExercicios(){
        
       
       exercicioControler = new PersonalExercicioControle(personal, exercicio);
        
        exercicios = exercicioControler.visualizarExercicios();
        
        if(exercicios != null){
            DefaultTableModel tbm = (DefaultTableModel) jTable1.getModel();
        
            while (tbm.getRowCount() > 0) {
                tbm.removeRow(0);
            }
        
            for (int i =0 ; i < exercicios.size(); i++) {
                //Adiciona a linha de acordo com o contador
                tbm.addRow(new String[i]);
                //Coloca os valores do Objeto nas linhas e colunas apropriadas
                jTable1.setValueAt(exercicios.get(i).getId_exercicio(), i, 0);
                jTable1.setValueAt(exercicios.get(i).getExercicio(), i, 1);
                jTable1.setValueAt(exercicios.get(i).getCategoria(), i, 2);
                // Adiciona 1 ao contador
            }
        }
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int linha = jTable1.getSelectedRow();
        if (linha < 0) {
            JOptionPane.showMessageDialog(this, "Selecione um exercicio", "Linha", JOptionPane.WARNING_MESSAGE);
        } else {
            new FormularioModificarExercicio(exercicios.get(linha), personal).setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

