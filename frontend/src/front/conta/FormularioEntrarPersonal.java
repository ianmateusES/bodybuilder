        personal.setSenha(jPasswordField1.getText());
        
        PersonalControle controle = new PersonalControle(personal);
     
        String resposta = controle.entrarConta();
        
        if(resposta.equals("sucesso")){
            new MenuPersonal(personal).setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this, resposta, "Mensagem do Sistema", JOptionPane.ERROR_MESSAGE);
        }
        
