    private void btn_pesquisar_alunos_principalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_pesquisar_alunos_principalActionPerformed
        String input = input_pesquisar_alunos_principal.getText();
        boolean invativo = check_alunos_inativos.isSelected();
        boolean ativo = check_alunos_inativos.isSelected();
        
        ArrayList<Aluno> alunos_pesquisados = new ArrayList<Aluno>();
        
        if(!input.isEmpty() && invativo){
            for(int i = 0; i < this.alunos.size(); i++){
                if(!this.alunos.get(i).isStatus()){
                    alunos_pesquisados.add(this.alunos.get(i));
                }
            }
        }else if(!input.isEmpty() && ativo){
            for(int i = 0; i < this.alunos.size(); i++){
                if(this.alunos.get(i).isStatus()){
                    alunos_pesquisados.add(this.alunos.get(i));
                }
            }    
        }else if(input.isEmpty() && invativo){
            for(int i = 0; i < this.alunos.size(); i++){
                if(!this.alunos.get(i).isStatus() && input.equalsIgnoreCase(this.alunos.get(i).getName())){
                    alunos_pesquisados.add(this.alunos.get(i));
                }
            }    
        }else if(input.isEmpty() && ativo){
            for(int i = 0; i < this.alunos.size(); i++){
                if(!this.alunos.get(i).isStatus() && input.equalsIgnoreCase(this.alunos.get(i).getName())){
                    alunos_pesquisados.add(this.alunos.get(i));
                }
            }    
        }else{
            preencherTabelaAlunos(panel_controle_alunos_principal, this.alunos);
        }
    }//GEN-LAST:event_btn_pesquisar_alunos_principalActionPerformed

