    private void preencherTabelaAlunos(JPanel panel, ArrayList<Aluno> alunos){
        for(int i = 0; i < alunos.size(); i++){
            String texto = i+"   "+ alunos.get(i).getName()+ "   "+alunos.get(i).getEmail();
            JFrame verAluno = new CadastrarExercicioVisual();
            adicionarLinha(panel, i, texto, verAluno);
        }
    }
    
    private void preencherTabelaExercicio(JPanel panel, ArrayList<Exercicio> exercicio){
        for(int i = 0; i < exercicio.size(); i++){
            String texto = i+"   "+ exercicio.get(i).getName()+ "   "+exercicio.get(i).getGroup()+"   "+exercicio.get(i).getAuthor();
            JFrame verExercicio = new CadastrarExercicioVisual();
            adicionarLinha(panel, i, texto, verExercicio);
        }
    }
    
    private String numeroAniversariantes(ArrayList<Aluno> alunos){
        long millis= System.currentTimeMillis();  
        java.sql.Date data = new java.sql.Date(millis);  

        String[] data_hoje = data.toString().split("-");
        String mes_dia_hoje = data_hoje[1]+data_hoje[2];
        
        int aniversariantes = 0;
        for(int i = 0; i < alunos.size(); i++){
            String[] data_aniversario = alunos.get(i).getBirthday().split("-");
            String mes_dia_aniversario = data_aniversario[1] + data_aniversario[2];
                    
            if(mes_dia_hoje.equals(mes_dia_aniversario)){
                aniversariantes++;
            }
        }
        
        return String.valueOf(aniversariantes);
    }
    
    private void adicionarLinha(JPanel panel, int id, String texto, JFrame abrir_clicar){
        
        JPanel novo_pane = new JPanel();
        novo_pane.setBackground(new java.awt.Color(68, 68, 68));
        novo_pane.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(254, 254, 254), 2));
        novo_pane.setForeground(new java.awt.Color(68, 68, 68));
        novo_pane.setLayout(null);
        
        Label novo_label = new Label();
        novo_label.setAlignment(java.awt.Label.LEFT);
        novo_label.setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR));
        novo_label.setFont(new java.awt.Font("Courier 10 Pitch", 0, 18)); // NOI18N
        novo_label.setForeground(new java.awt.Color(254, 254, 254));
        novo_label.setText(texto);
        novo_label.setBounds(10, 10, 930, 30);
        
        novo_label.addMouseListener(new MouseAdapter(){  
            public void mouseClicked(MouseEvent e){  
                abrir_clicar.setVisible(true);
            }  
        });
        
        novo_pane.add(novo_label);

        int insertAt = Math.max(0, panel.getComponentCount() - 1);
        GridBagConstraints gbc = new GridBagConstraints();
        
        gbc.gridx = 0;
        gbc.gridy = id; // muda de acordo com o negócio
        gbc.ipadx = 985;
        gbc.ipady = 44;
        gbc.insets = new Insets(2, 2, 0, 2);
        gbc.anchor = GridBagConstraints.NORTHWEST;
        gbc.weighty = 1;

        panel.add(novo_pane, gbc, insertAt);
        panel.revalidate();
        panel.repaint();
    }
    
