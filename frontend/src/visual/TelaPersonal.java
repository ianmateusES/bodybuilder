    private void mudarFonte(String link){
        Component[] components = panel_opcoes.getComponents();
        for (int i=0; i < components.length; i++) {
            Color comp = new Color (220,220,220);
            if(!components[i].getName().equalsIgnoreCase("txt_navegacao")){
                comp = (components[i].getName().equalsIgnoreCase(link))? new Color (152,152,152) : new Color (254,254,254);
            }
            components[i].setForeground(comp);
        }
    }


