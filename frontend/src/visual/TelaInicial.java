    private void mudarTexto(TextField input, String txt_antigo, String txt_novo){
        if(input.getText().equalsIgnoreCase(txt_antigo)){
            input.setText(txt_novo);
        }
    }
    
    private void mudarTexto(JTextField input, String txt_antigo, String txt_novo){
        if(input.getText().equalsIgnoreCase(txt_antigo)){
            input.setText(txt_novo);
        }
    }
    
    private void mudarTextoSenha(TextField input, String txt_antigo, String txt_novo){
        if(input.getText().equalsIgnoreCase(txt_antigo)){
            input.setText(txt_novo);
            input.setEchoChar('\u0000');
        }
    }
