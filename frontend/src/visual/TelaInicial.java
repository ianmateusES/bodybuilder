    private void btn_entrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_entrarActionPerformed
        String email = input_email_login.getText();
        String senha = input_senha_login.getText();
        
        try {
            SessionController.login(email, senha);

            if(PersonalLogado.getInstance().getToken().isEmpty()){
                System.out.println("Erro");
            }else{
                TelaPersonal a = new TelaPersonal();
                a.setVisible(true);
                this.dispose();
            }
            
        } catch (Exception ex) {
            Logger.getLogger(TelaInicial.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btn_entrarActionPerformed

    private void btn_cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cadastrarActionPerformed
        String nome = input_nome_cadastrar.getText();
        String endereco = input_endereco_cadastrar.getText();
        
        String telefone = input_telefone_cadastrar.getText();
        String data = input_data_nasc_cadastrar.getText();
        
       // String profissao = input_profissao_cadastrar.getText();
        String cr = input_cr_cadastrar.getText();
        
        String email = input_email_cadastrar.getText();

        String senha = input_senha_cadastrar.getText();
        String confirmar_senha = input_confirmar_senha_cadastrar.getText();
        
        
        try {
            Personal personal = PersonalController.cadastro(nome, email, senha, confirmar_senha, data, telefone, cr, endereco);
            
            if(personal.getId().isEmpty()){
                System.out.println("Erro");
            }else{
                System.out.println("Cadastrado");
            }
            
        } catch (Exception ex) {
            Logger.getLogger(TelaInicial.class.getName()).log(Level.SEVERE, null, ex);
        }
     
        
        
    }//GEN-LAST:event_btn_cadastrarActionPerformed

